#python tem tipos dinâmicos de atribuição de variáveis, permitindo a mudança delas
#atribuindo à variável de resultado um outro valor, o print vai escrever a última atribuição também, corretamente, mesmo com o mesmo nome na variável, considerando ambas
#posso fazer operações com minhas atribuições, exemplo:

resultado = 2
resultado += resultado
resultado += 3 #resultado = resultado + 3
resultado -= 1 #resultado = resultado - 1
resultado *= 4 #resultado = resultado * 4
resultado /= 2 #resultado = resultado / 2
resultado %= 6 ##resultado = resultado % 6
print(resultado)

